export default function SupportTicketsPage() {
  const [selectedTicket, setSelectedTicket] = useState<SupportTicket | null>(null)
  const [isDialogOpen, setIsDialogOpen] = useState(false)
  const [replyText, setReplyText] = useState("")

  const columns: ColumnDef<SupportTicket>[] = [
    {
      accessorKey: "subject",
      header: "Subject",
      cell: ({ row }) => {
        const subject = row.getValue("subject") as string
        return <div className="font-medium">{subject}</div>
      },
    },
    {
      accessorKey: "createdAt",
      header: ({ column }) => {
        return (
          <Button
            variant="ghost"
            onClick={() => column.toggleSorting(column.getIsSorted() === "asc")}
            className="p-0 hover:bg-transparent"
          >
            Created At
            <ArrowUpDown className="ml-2 h-4 w-4" />
          </Button>
        )
      },
      cell: ({ row }) => {
        const date = new Date(row.getValue("createdAt"))
        return <div>{date.toLocaleString()}</div>
      },
    },
    {
      accessorKey: "status",
      header: "Status",
      cell: ({ row }) => {
        const status = row.getValue("status") as string

        return (
          <Badge variant={status === "open" ? "default" : status === "in-progress" ? "secondary" : "outline"}>
            {status}
          </Badge>
        )
      },
    },
    {
      accessorKey: "assignedTo",
      header: "Assigned To",
      cell: ({ row }) => {
        const assignedTo = row.getValue("assignedTo") as string
        return <div>{assignedTo}</div>
      },
    },
    {
      id: "actions",
      cell: ({ row }) => {
        const ticket = row.original

        return (
          <Button
            variant="outline"
            size="sm"
            className="h-8 gap-1"
            onClick={() => {
              setSelectedTicket(ticket)
              setIsDialogOpen(true)
            }}
          >
            <MessageSquare className="h-4 w-4" />
            View / Reply
          </Button>
        )
      },
    },
  ]

  const handleReply = () => {
    // In a real app, this would send the reply to the backend
    console.log("Replying to ticket:", selectedTicket?.id, "with:", replyText)
    setReplyText("")
    // Close the dialog or keep it open as needed
    // setIsDialogOpen(false)
  }

  return (
    <div className="space-y-6">
      <div>
        <h1 className="text-3xl font-bold tracking-tight">Support Tickets</h1>
        <p className="text-muted-foreground">Manage and respond to user support requests</p>
      </div>

      <Tabs defaultValue="all" className="space-y-4">
        <TabsList>
          <TabsTrigger value="all">All Tickets</TabsTrigger>
          <TabsTrigger value="open">Open</TabsTrigger>
          <TabsTrigger value="in-progress">In Progress</TabsTrigger>
          <TabsTrigger value="closed">Closed</TabsTrigger>
        </TabsList>

        <TabsContent value="all">
          <Card>
            <CardHeader>
              <CardTitle>All Support Tickets</CardTitle>
            </CardHeader>
            <CardContent>
              <DataTable columns={columns} data={data} searchColumn="subject" searchPlaceholder="Search tickets..." />
            </CardContent>
          </Card>
        </TabsContent>

        <TabsContent value="open">
          <Card>
            <CardHeader>
              <CardTitle>Open Tickets</CardTitle>
            </CardHeader>
            <CardContent>
              <DataTable
                columns={columns}
                data={data.filter((ticket) => ticket.status === "open")}
                searchColumn="subject"
                searchPlaceholder="Search open tickets..."
              />
            </CardContent>
          </Card>
        </TabsContent>

        <TabsContent value="in-progress">
          <Card>
            <CardHeader>
              <CardTitle>In Progress Tickets</CardTitle>
            </CardHeader>
            <CardContent>
              <DataTable
                columns={columns}
                data={data.filter((ticket) => ticket.status === "in-progress")}
                searchColumn="subject"
                searchPlaceholder="Search in-progress tickets..."
              />
            </CardContent>
          </Card>
        </TabsContent>

        <TabsContent value="closed">
          <Card>
            <CardHeader>
              <CardTitle>Closed Tickets</CardTitle>
            </CardHeader>
            <CardContent>
              <DataTable
                columns={columns}
                data={data.filter((ticket) => ticket.status === "closed")}
                searchColumn="subject"
                searchPlaceholder="Search closed tickets..."
              />
            </CardContent>
          </Card>
        </TabsContent>
      </Tabs>

      <Dialog open={isDialogOpen} onOpenChange={setIsDialogOpen}>
        <DialogContent className="max-w-3xl max-h-[80vh] overflow-y-auto">
          <DialogHeader>
            <DialogTitle>{selectedTicket?.subject}</DialogTitle>
            <DialogDescription>
              Ticket #{selectedTicket?.id} â€¢ {selectedTicket?.status}
            </DialogDescription>
          </DialogHeader>

          {selectedTicket && (
            <>
              <div className="space-y-4">
                <div className="bg-muted p-4 rounded-md">
                  <h3 className="text-sm font-medium mb-2">Description</h3>
                  <p className="text-sm">{selectedTicket.description}</p>
                </div>

                <div className="space-y-4">
                  <h3 className="text-sm font-medium">Conversation</h3>

                  {selectedTicket.messages.map((message, index) => (
                    <div key={index} className="flex gap-4">
                      <Avatar>
                        <AvatarFallback>
                          {message.sender
                            .split(" ")
                            .map((n) => n[0])
                            .join("")}
                        </AvatarFallback>
                      </Avatar>
                      <div className="space-y-1">
                        <div className="flex items-center gap-2">
                          <span className="text-sm font-medium">{message.sender}</span>
                          <span className="text-xs text-muted-foreground">
                            {new Date(message.timestamp).toLocaleString()}
                          </span>
                        </div>
                        <p className="text-sm">{message.message}</p>
                      </div>
                    </div>
                  ))}
                </div>
              </div>

              <div className="space-y-2">
                <h3 className="text-sm font-medium">Reply</h3>
                <Textarea
                  placeholder="Type your reply here..."
                  value={replyText}
                  onChange={(e) => setReplyText(e.target.value)}
                  rows={4}
                />
              </div>

              <DialogFooter>
                <Button variant="outline" onClick={() => setIsDialogOpen(false)}>
                  Cancel
                </Button>
                <Button onClick={handleReply} disabled={!replyText.trim()}>
                  Send Reply
                </Button>
              </DialogFooter>
            </>
          )}
        </DialogContent>
      </Dialog>
    </div>
  )
}
