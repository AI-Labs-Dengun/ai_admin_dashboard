import { NextResponse } from 'next/server';
import { createRouteHandlerClient } from '@supabase/auth-helpers-nextjs';
import { cookies } from 'next/headers';
import { verifyBotToken } from '@/app/(dashboard)/dashboard/lib/jwtManagement';

export async function GET(
  request: Request,
  { params }: { params: Promise<{ botId: string }> }
) {
  try {
    const { botId } = await params;
    console.log('üîç Iniciando busca de detalhes do bot:', botId);

    const supabase = createRouteHandlerClient({ cookies });

    // Verificar autentica√ß√£o
    const { data: { session } } = await supabase.auth.getSession();
    if (!session) {
      console.error('‚ùå Usu√°rio n√£o autenticado');
      return NextResponse.json(
        { error: 'N√£o autorizado' },
        { status: 401 }
      );
    }

    console.log('üë§ Usu√°rio autenticado:', session.user.id);

    // Buscar detalhes do bot atrav√©s da view user_bots_details
    console.log('üîç Buscando detalhes do bot:', botId);
    const { data: bot, error: botError } = await supabase
      .from('user_bots_details')
      .select('*')
      .eq('bot_id', botId)
      .single();

    if (botError) {
      console.error('‚ùå Erro ao buscar bot:', { 
        error: botError, 
        botId 
      });
      return NextResponse.json(
        { error: 'Erro ao buscar detalhes do bot' },
        { status: 500 }
      );
    }

    if (!bot) {
      console.error('‚ùå Bot n√£o encontrado:', { 
        botId,
        userId: session.user.id 
      });
      return NextResponse.json(
        { error: 'Bot n√£o encontrado' },
        { status: 404 }
      );
    }

    console.log('‚úÖ Bot encontrado:', { 
      id: bot.bot_id, 
      name: bot.bot_name 
    });

    // Verificar se o usu√°rio tem acesso ao bot
    console.log('üîç Verificando acesso do usu√°rio ao bot:', { 
      userId: session.user.id, 
      botId 
    });
    const { data: userBot, error: userBotError } = await supabase
      .from('user_bots')
      .select('enabled')
      .match({
        user_id: session.user.id,
        bot_id: botId
      })
      .single();

    if (userBotError) {
      console.error('‚ùå Erro ao verificar acesso do usu√°rio:', { 
        error: userBotError, 
        userId: session.user.id, 
        botId 
      });
      return NextResponse.json(
        { error: 'Erro ao verificar acesso ao bot' },
        { status: 500 }
      );
    }

    if (!userBot?.enabled) {
      console.error('‚ùå Usu√°rio n√£o tem acesso ao bot:', { 
        userId: session.user.id, 
        botId 
      });
      return NextResponse.json(
        { error: 'Acesso n√£o permitido a este bot' },
        { status: 403 }
      );
    }

    console.log('‚úÖ Acesso do usu√°rio validado');

    // Retornar os dados do bot formatados
    return NextResponse.json({
      id: bot.bot_id,
      name: bot.bot_name,
      description: bot.bot_description,
      bot_capabilities: bot.bot_capabilities || [],
      contact_email: bot.admin_email,
      website: bot.bot_website,
      max_tokens_per_request: bot.token_limit,
      created_at: bot.created_at
    });
  } catch (error) {
    console.error('‚ùå Erro interno:', error);
    return NextResponse.json(
      { error: 'Erro interno ao buscar detalhes do bot' },
      { status: 500 }
    );
  }
} 